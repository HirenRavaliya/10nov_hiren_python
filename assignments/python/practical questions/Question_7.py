# Q7 - Functions and Methods (String Manipulation)

# Define one base string used for most examples
my_string = "PythonProgramming"

# Practical Example 1
print("Example 1: ", "Hello")

# Practical Example 2
greeting = "Welcome to Python"
print("Example 2:", greeting)

# Practical Example 3
triple_quoted_str = """This is a string
written using triple quotes."""
print("Example 3:", triple_quoted_str)

# Practical Example 4: First character (index 0)
print("Example 4: First character:", my_string[0])

# Practical Example 5: From second position onwards (index 1 to end)
print("Example 5: From second position onwards:", my_string[1:])

# Practical Example 6: Up to the fifth character (index 0 to 4)
print("Example 6: Up to fifth character:", my_string[:5])

# Practical Example 7: Substring between index 1 and 4 (1,2,3,4)
print("Example 7: Substring [1:5]:", my_string[1:5])

# Practical Example 8: String from the last character (reverse using slicing)
print("Example 8: String reversed:", my_string[::-1])

# Practical Example 9: Every alternate character starting from index 1
print("Example 9: Every alternate character from index 1:", my_string[1::2])
